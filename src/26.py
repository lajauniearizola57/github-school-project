def greet_user(name):
    """
    A simple function to greet the user with their name.
    
    Parameters:
        name (str): The name of the user.
        
    Returns:
        str: A message greeting the user.
    """
    return f"Hello, {name}!"

# Example usage:
user_name = "Alice"
print(greet_user(user_name))
